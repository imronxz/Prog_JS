// The remainder Operator -> Nilai sisa
console.log(5 % 2);
console.log(5 / 2); // 5 = 2 * 2 + 1

console.log(8 % 3);
console.log(8 / 3); // 8 = 2 * 3 + 2

console.log(6 % 2);
console.log(6 / 2);

const isEven = n => n % 2 === 0;
const isEvent = function (n) {
  return n % 2 === 0;
};
console.log(isEvent(8));
console.log(isEven(23));
console.log(isEven(5148));
